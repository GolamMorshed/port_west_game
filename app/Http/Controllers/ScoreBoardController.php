     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $submit_score = new ScoreBoard();
        $submit_score->user_id = $request->user_id;
        $submit_score->name = $request->name;
        $submit_score->score = $request->score;
        $submit_score->game_start_time = $request->game_start_time;

        if($submit_score->save()){
            return new ScoreBoardResource($submit_score);
        }

    }

